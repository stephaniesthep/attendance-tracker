// Simple in-memory cache for user data to reduce database calls
// In production, consider using Redis or similar for distributed caching

interface CacheEntry<T> {
  data: T;
  timestamp: number;
  ttl: number; // Time to live in milliseconds
}

class SimpleCache {
  private cache = new Map<string, CacheEntry<any>>();
  private readonly DEFAULT_TTL = 5 * 60 * 1000; // 5 minutes

  set<T>(key: string, data: T, ttl: number = this.DEFAULT_TTL): void {
    this.cache.set(key, {
      data,
      timestamp: Date.now(),
      ttl,
    });
  }

  get<T>(key: string): T | null {
    const entry = this.cache.get(key);
    if (!entry) return null;

    const now = Date.now();
    if (now - entry.timestamp > entry.ttl) {
      this.cache.delete(key);
      return null;
    }

    return entry.data as T;
  }

  delete(key: string): void {
    this.cache.delete(key);
  }

  clear(): void {
    this.cache.clear();
  }

  // Clean up expired entries
  cleanup(): void {
    const now = Date.now();
    for (const [key, entry] of this.cache.entries()) {
      if (now - entry.timestamp > entry.ttl) {
        this.cache.delete(key);
      }
    }
  }
}

export const userCache = new SimpleCache();

// Clean up expired entries every 10 minutes
setInterval(() => {
  userCache.cleanup();
}, 10 * 60 * 1000);

// Cache keys
export const getCacheKey = {
  user: (userId: string) => `user:${userId}`,
  userWithPermissions: (userId: string) => `user_permissions:${userId}`,
  userRoles: (userId: string) => `user_roles:${userId}`,
};